(function initEconovariaPlayerCreateUx() {
  "use strict";

  const FORM_SELECTOR = "[data-admin-terminal-player-form]";
  const CREATE_ACTION_SELECTOR = '[data-admin-terminal-action="create-player"]';
  const LEGACY_DIALOG_SELECTOR = "[data-admin-player-access-code-dialog]";
  const CONFIRMATION_SELECTOR = "[data-admin-player-created-confirmation]";
  const STYLE_ID = "econovaria-player-create-ux-style";
  const ALPHABET = "ABCDEFGHJKLMNPQRSTUVWXYZ23456789";
  let lastCreateContext = null;

  function text(value) {
    return String(value ?? "").trim();
  }

  function randomToken(length) {
    const bytes = new Uint8Array(length);
    window.crypto.getRandomValues(bytes);
    return Array.from(bytes, (value) => ALPHABET[value % ALPHABET.length]).join("");
  }

  function generatePlayerIdentifier() {
    return `PLR-${randomToken(8)}`;
  }

  function generateAccessCode() {
    return `${randomToken(4)}-${randomToken(4)}`;
  }

  function field(form, name) {
    return form?.querySelector?.(`[name="${name}"]`) || null;
  }

  function emitInput(input) {
    input.dispatchEvent(new Event("input", { bubbles: true }));
    input.dispatchEvent(new Event("change", { bubbles: true }));
  }

  function setFieldHelp(input, message) {
    const label = input?.closest?.("label, .admin-terminal-field");
    if (!label) return;
    let helper = label.querySelector("[data-admin-player-create-auto-help]");
    if (!helper) {
      helper = document.createElement("small");
      helper.setAttribute("data-admin-player-create-auto-help", "");
      label.append(helper);
    }
    helper.textContent = message;
  }

  function configureCreateForm(form) {
    if (!(form instanceof Element)) return false;
    const identifier = field(form, "playerIdentifier");
    const accessCode = field(form, "accessCode");
    if (!(identifier instanceof HTMLInputElement) || !(accessCode instanceof HTMLInputElement)) return false;

    identifier.required = false;
    identifier.removeAttribute("required");
    identifier.placeholder = "Leave blank to auto-generate a Player ID";
    setFieldHelp(identifier, "Optional. Leave blank to generate a unique Player ID automatically.");

    accessCode.required = false;
    accessCode.removeAttribute("required");
    accessCode.placeholder = "Leave blank to auto-generate an Access Code";
    setFieldHelp(accessCode, "Optional. Leave blank to generate a secure one-time Access Code automatically.");

    const helper = form.querySelector(".admin-terminal-player-settings-head small");
    if (helper) {
      helper.textContent = "Enter custom sign-in credentials or leave either field blank to generate it securely when the player is created. The UUID remains backend-only.";
    }
    form.setAttribute("data-admin-player-create-auto-credentials", "");
    return true;
  }

  function prepareCreateCredentials(form) {
    if (!configureCreateForm(form)) return null;
    const identifier = field(form, "playerIdentifier");
    const accessCode = field(form, "accessCode");
    const generatedIdentifier = !text(identifier.value);
    const generatedAccessCode = !text(accessCode.value);

    if (generatedIdentifier) {
      identifier.value = generatePlayerIdentifier();
      emitInput(identifier);
    }
    if (generatedAccessCode) {
      accessCode.value = generateAccessCode();
      emitInput(accessCode);
    }

    identifier.dataset.autoGeneratedCredential = generatedIdentifier ? "true" : "false";
    accessCode.dataset.autoGeneratedCredential = generatedAccessCode ? "true" : "false";
    lastCreateContext = {
      playerIdentifier: text(identifier.value),
      accessCode: text(accessCode.value),
      generatedIdentifier,
      generatedAccessCode,
    };
    return lastCreateContext;
  }

  function ensureStyles() {
    if (document.getElementById(STYLE_ID)) return;
    const style = document.createElement("style");
    style.id = STYLE_ID;
    style.textContent = `
      ${LEGACY_DIALOG_SELECTOR} { display: none !important; }
      [data-admin-player-create-auto-help] {
        display:block;margin-top:4px;color:rgba(190,205,207,.68)!important;
        font-size:8px!important;line-height:1.35!important;letter-spacing:0!important;text-transform:none!important;
      }
      .admin-terminal-player-created-modal { width:min(720px,calc(100vw - 32px)); }
      .admin-terminal-player-created-summary { display:grid;grid-template-columns:repeat(2,minmax(0,1fr));gap:12px; }
      .admin-terminal-player-created-credential {
        min-width:0;padding:16px;border:1px solid rgba(0,212,255,.24);background:rgba(0,212,255,.045);
      }
      .admin-terminal-player-created-credential span,
      .admin-terminal-player-created-credential small {
        display:block;color:rgba(210,247,255,.58);font:800 9px/1.35 "JetBrains Mono",ui-monospace,monospace;
        letter-spacing:.12em;text-transform:uppercase;
      }
      .admin-terminal-player-created-credential strong {
        display:block;overflow-wrap:anywhere;margin:10px 0 8px;color:#e9fbff;
        font:800 20px/1.2 "JetBrains Mono",ui-monospace,monospace;letter-spacing:.08em;user-select:all;
      }
      .admin-terminal-player-created-warning {
        margin:14px 0 0;padding:11px 13px;border:1px solid rgba(255,103,0,.3);
        background:rgba(255,103,0,.055);color:rgba(233,251,255,.72);font-size:11px;line-height:1.5;
      }
      .admin-terminal-player-created-actions { display:flex;justify-content:flex-end;gap:10px;padding:0 20px 20px; }
      .admin-terminal-player-created-actions button {
        min-height:42px;padding:0 16px;border:1px solid rgba(0,212,255,.34);color:#d2f7ff;
        background:rgba(8,29,45,.94);cursor:pointer;font:800 10px/1.2 "JetBrains Mono",ui-monospace,monospace;
        letter-spacing:.08em;text-transform:uppercase;
      }
      .admin-terminal-player-created-actions [data-admin-player-created-done] {
        border-color:rgba(255,103,0,.72);color:#071421;background:#ff6700;
      }
      @media (max-width:720px) {
        .admin-terminal-player-created-summary { grid-template-columns:1fr; }
        .admin-terminal-player-created-actions { flex-direction:column; }
        .admin-terminal-player-created-actions button { width:100%; }
      }
    `;
    document.head.append(style);
  }

  function removeLegacyDialog() {
    document.querySelectorAll(LEGACY_DIALOG_SELECTOR).forEach((node) => node.remove());
  }

  async function copyCredentials(detail, button) {
    const value = `Player ID: ${text(detail.playerIdentifier)}\nAccess Code: ${text(detail.studentCode)}`;
    try {
      await navigator.clipboard.writeText(value);
      button.textContent = "Credentials copied";
    } catch (_) {
      button.textContent = "Copy unavailable";
    }
  }

  function credentialCard(label, value, note, valueAttribute) {
    const card = document.createElement("article");
    card.className = "admin-terminal-player-created-credential";
    const caption = document.createElement("span");
    caption.textContent = label;
    const strong = document.createElement("strong");
    strong.setAttribute(valueAttribute, "");
    strong.textContent = value || "—";
    const small = document.createElement("small");
    small.textContent = note;
    card.append(caption, strong, small);
    return card;
  }

  function renderConfirmation(detail, context) {
    if (!document.body || !text(detail?.studentCode)) return;
    removeLegacyDialog();
    document.querySelector(CONFIRMATION_SELECTOR)?.remove();

    const backdrop = document.createElement("div");
    backdrop.className = "admin-terminal-modal-backdrop admin-terminal-player-created-backdrop";
    backdrop.setAttribute("data-admin-terminal-modal-backdrop", "");
    backdrop.setAttribute("data-modal-id", "player-created-confirmation");
    backdrop.setAttribute("data-admin-player-created-confirmation", "");

    const modal = document.createElement("section");
    modal.className = "admin-terminal-modal admin-terminal-player-created-modal";
    modal.setAttribute("role", "dialog");
    modal.setAttribute("aria-modal", "true");
    modal.setAttribute("aria-labelledby", "adminPlayerCreatedTitle");

    const header = document.createElement("header");
    header.className = "admin-terminal-modal-head";
    const heading = document.createElement("div");
    const kicker = document.createElement("span");
    kicker.textContent = "Player created";
    const title = document.createElement("h3");
    title.id = "adminPlayerCreatedTitle";
    title.textContent = text(detail.displayName) || "New player";
    const description = document.createElement("p");
    description.textContent = context.generatedIdentifier || context.generatedAccessCode
      ? "The player was created successfully. Generated sign-in credentials are shown below."
      : "The player was created successfully with the sign-in credentials you entered.";
    heading.append(kicker, title, description);

    const close = document.createElement("button");
    close.className = "admin-terminal-modal-close admin-terminal-modal-top-close-v474";
    close.type = "button";
    close.setAttribute("aria-label", "Close confirmation");
    close.setAttribute("data-admin-terminal-modal-close", "");
    close.textContent = "×";
    header.append(heading, close);

    const body = document.createElement("div");
    body.className = "admin-terminal-modal-body";
    const summary = document.createElement("section");
    summary.className = "admin-terminal-player-created-summary";
    summary.append(
      credentialCard(
        "Player ID / RFID",
        text(detail.playerIdentifier) || context.playerIdentifier,
        context.generatedIdentifier ? "Generated automatically" : "Custom value saved",
        "data-admin-player-created-identifier",
      ),
      credentialCard(
        "Access Code",
        text(detail.studentCode) || context.accessCode,
        context.generatedAccessCode ? "Generated automatically" : "Custom value saved",
        "data-admin-player-created-access-code",
      ),
    );
    const warning = document.createElement("p");
    warning.className = "admin-terminal-player-created-warning";
    warning.textContent = "Save or copy the Access Code now. After this confirmation closes, only its secure hash remains stored and the code cannot be displayed again.";
    body.append(summary, warning);

    const actions = document.createElement("footer");
    actions.className = "admin-terminal-player-created-actions";
    const copy = document.createElement("button");
    copy.type = "button";
    copy.setAttribute("data-admin-player-created-copy", "");
    copy.textContent = "Copy credentials";
    copy.addEventListener("click", () => void copyCredentials(detail, copy));
    const done = document.createElement("button");
    done.type = "button";
    done.setAttribute("data-admin-player-created-done", "");
    done.textContent = "Done";
    actions.append(copy, done);

    modal.append(header, body, actions);
    backdrop.append(modal);
    backdrop.addEventListener("click", (event) => {
      const control = event.target instanceof Element
        ? event.target.closest("[data-admin-terminal-modal-close], [data-admin-player-created-done]")
        : null;
      if (event.target === backdrop || control) backdrop.remove();
    });
    document.body.append(backdrop);
    copy.focus();
  }

  function configureWithin(root = document) {
    const forms = [];
    if (root instanceof Element && root.matches(FORM_SELECTOR)) forms.push(root);
    forms.push(...(root.querySelectorAll?.(FORM_SELECTOR) || []));
    [...new Set(forms)].forEach(configureCreateForm);
  }

  ensureStyles();
  configureWithin();

  if (document.body && typeof MutationObserver === "function") {
    const observer = new MutationObserver((mutations) => {
      for (const mutation of mutations) {
        for (const node of mutation.addedNodes) {
          if (node instanceof Element && (node.matches(FORM_SELECTOR) || node.querySelector(FORM_SELECTOR))) {
            window.setTimeout(() => configureWithin(node), 0);
          }
        }
      }
      removeLegacyDialog();
    });
    observer.observe(document.body, { childList: true, subtree: true });
  }

  document.addEventListener("click", (event) => {
    const action = event.target instanceof Element ? event.target.closest(CREATE_ACTION_SELECTOR) : null;
    if (!action) return;
    const form = action.closest(FORM_SELECTOR) || document.querySelector(FORM_SELECTOR);
    if (form) prepareCreateCredentials(form);
  }, true);

  document.addEventListener("submit", (event) => {
    const form = event.target instanceof Element && event.target.matches(FORM_SELECTOR) ? event.target : null;
    if (form) prepareCreateCredentials(form);
  }, true);

  window.addEventListener("econovaria:player-access-code-issued", (event) => {
    const context = lastCreateContext;
    if (!context) return;
    const detail = event?.detail || {};
    lastCreateContext = null;
    window.setTimeout(() => {
      removeLegacyDialog();
      renderConfirmation(detail, context);
    }, 0);
  }, { capture: true });

  window.EconovariaPlayerCreateUx = {
    configureCreateForm,
    prepareCreateCredentials,
    generatePlayerIdentifier,
    generateAccessCode,
    renderConfirmation,
  };
})();